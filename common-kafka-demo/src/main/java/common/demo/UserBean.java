package common.demo;

import java.io.Serializable;

/**
 * @author zhangjj
 * @create 2017-12-26 22:39
 **/
public class UserBean implements Serializable{
    private int id;

    private String name;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "UserBean{" +
                "id=" + id +
                ", name='" + name + '\'' +
                '}';
    }
}
